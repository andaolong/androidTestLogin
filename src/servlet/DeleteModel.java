package servlet;

import db.DBUtils;
import domain.MessageBean;

public class DeleteModel {
	public MessageBean deleteModel02(String userName,String password,String modelName) {
		
		MessageBean messageBean = new MessageBean();
		// 返回信息类对象，回传给客户端的json对象，包含code，message，data,data在这里为返回的ModelBean
		
		
		// 请求数据库
		DBUtils dbUtils = new DBUtils();
		// 打开数据库连接
		dbUtils.openConnect();
		
		//处理部分在数据库isRightUserInDB()方法里面，直接把该方法返回的messageBean作为返回参数传给DealCmd即可
		messageBean=dbUtils.deleteModelInDB(userName,password,modelName);	
		
		dbUtils.closeConnect(); // 关闭数据库连接
		
		return messageBean;
	}
}
